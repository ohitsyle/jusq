export async function exportCampaigns(dateFilter = {}) {
  const PromotionCampaign = (await import('../models/PromotionCampaign.js')).default;
  const campaigns = await PromotionCampaign.find(withDateFilter({}, dateFilter)).sort({ createdAt: -1 }).lean();
  const rewardLabel = (c) => {
    const v = c.rewardValue ?? 1;
    if (c.rewardType === 'discount') return `${v}% Off`;
    if (c.rewardType === 'credit') return `₱${v} Credit`;
    return v > 1 ? `${v} Free Rides` : 'Free Ride';
  };
  const headers = ['Title', 'Description', 'Reward Type', 'Reward', 'Minimum Rides', 'Frequency', 'Active', 'Rewards Sent', 'Last Run', 'Created'];
  const rows = campaigns.map((c) => ({
    'Title': c.title,
    'Description': c.description || '',
    'Reward Type': c.rewardType || '',
    'Reward': rewardLabel(c),
    'Minimum Rides': c.minimumRides ?? '',
    'Frequency': c.frequency || '',
    'Active': yesNo(c.active),
    'Rewards Sent': c.rewardsSent || 0,
    'Last Run': fmtDate(c.lastRunDate),
    'Created': fmtDate(c.createdAt),
  }));
  return { title: 'Promotional Campaigns Report', headers, rows };
}

// ---------------------------------------------------------------------------
// Dispatcher — the ONE entry point used everywhere.
// Returns { csv, count } where csv already carries the branded document header.
// ---------------------------------------------------------------------------
export async function exportByType(exportType, dateFilter = {}, role = null, meta = {}) {
  const normalizedType = String(exportType).toLowerCase().replace(/\s+/g, '-');

  let result;
  switch (normalizedType) {
    case 'drivers': result = await exportDrivers(dateFilter); break;
    case 'shuttles': result = await exportShuttles(dateFilter); break;
    case 'routes': result = await exportRoutes(dateFilter); break;
    case 'phones': result = await exportPhones(dateFilter, role); break;
    case 'trips': result = await exportTrips(dateFilter); break;
    case 'transactions': result = await exportTransactions(dateFilter); break;
    case 'logs': result = await exportLogs(dateFilter, role); break;
    case 'users': result = await exportUsers(dateFilter); break;
    case 'concerns': result = await exportConcerns(dateFilter, role); break;
    case 'merchants': result = await exportMerchants(dateFilter); break;
    case 'cash-ins':
    case 'cashins': result = await exportCashIns(dateFilter); break;
    case 'balances': result = await exportBalances(dateFilter); break;
    case 'admins': result = await exportAdmins(dateFilter); break;
    case 'campaigns': result = await exportCampaigns(dateFilter); break;
    default: throw new Error(`Unknown export type: ${exportType}`);
  }

  const csv = buildBrandedCsv(result.title, result.headers, result.rows, meta);
  return { csv, count: result.rows.length, title: result.title };
}

// Friendly department label for the branded "Generated by" line.
export const roleDepartmentLabel = (role) => {
  const map = { sysad: 'System Administrator', treasury: 'Treasury', accounting: 'Accounting', marketing: 'Marketing', motorpool: 'Motorpool', merchant: 'Merchant Management' };
  return map[role] || (role ? role.charAt(0).toUpperCase() + role.slice(1) : '');
};
