// Build a userId -> "Full Name" map for transaction-style exports (Transaction
// docs store schoolUId/email but not the member's name).
async function nameMapForTransactions(txs) {
  const ids = [...new Set(txs.map((t) => t.userId).filter(Boolean).map(String))];
  if (ids.length === 0) return {};
  const users = await User.find({ _id: { $in: ids } }).select('firstName lastName').lean();
  return Object.fromEntries(users.map((u) => [String(u._id), `${u.firstName || ''} ${u.lastName || ''}`.trim()]));
}

export async function exportTransactions(dateFilter = {}) {
  const txs = await Transaction.find(withDateFilter({}, dateFilter)).sort({ createdAt: -1 }).lean();
  const names = await nameMapForTransactions(txs);
  const headers = ['Date', 'Time', 'ID Number', 'Name', 'Email', 'Type', 'Status', 'Amount', 'Balance After', 'Transaction ID'];
  const rows = txs.map((t) => ({
    'Date': fmtDate(t.createdAt),
    'Time': fmtTime(t.createdAt),
    'ID Number': t.schoolUId || '',
    'Name': names[String(t.userId)] || '',
    'Email': t.email || '',
    'Type': t.transactionType === 'credit' ? 'Cash-In' : 'Payment',
    'Status': t.status || '',
    'Amount': t.amount ?? 0,
    'Balance After': t.balance ?? '',
    'Transaction ID': t.transactionId || '',
  }));
  return { title: 'Transactions Report', headers, rows };
}

