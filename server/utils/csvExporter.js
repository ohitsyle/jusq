// ---------------------------------------------------------------------------
// Shared CSV builder — mirrors client/src/utils/csvExport.js exactly so a
// download from a tab and a download from Settings are byte-for-byte the same.
// ---------------------------------------------------------------------------

const esc = (v) => {
  if (v === null || v === undefined) return '';
  const s = String(v);
  return s.includes(',') || s.includes('"') || s.includes('\n')
    ? `"${s.replace(/"/g, '""')}"`
    : s;
};

const fmtDateTime = (d) => d ? new Date(d).toLocaleString('en-PH', {
  year: 'numeric', month: 'short', day: 'numeric', hour: '2-digit', minute: '2-digit'
}) : '';
const fmtDate = (d) => d ? new Date(d).toLocaleDateString('en-PH', { year: 'numeric', month: 'short', day: 'numeric' }) : '';
const fmtTime = (d) => d ? new Date(d).toLocaleTimeString('en-PH', { hour: '2-digit', minute: '2-digit' }) : '';
const yesNo = (b) => (b ? 'Yes' : 'No');

/**
 * Wrap column headers + row objects in the branded NUCash document header.
 * @param {string} title    report title, e.g. "Routes Report"
 * @param {string[]} headers ordered column labels (also the keys into each row)
 * @param {object[]} rows    row objects keyed by the header labels
 * @param {object} meta      { adminName, department, dateRange }
 * @returns {string} full CSV text with a UTF-8 BOM
 */
function buildBrandedCsv(title, headers, rows, meta = {}) {
  const dateStr = new Date().toLocaleString('en-PH', {
    year: 'numeric', month: 'long', day: 'numeric', hour: '2-digit', minute: '2-digit'
  });
  const byLine = [meta.adminName, meta.department ? `(${meta.department})` : null]
    .filter(Boolean).join(' ') || 'Admin';

  const docRows = [
    'NUCash University Cashless Payment System',
    `Report: ${title}`,
    `Generated by: ${byLine}`,
    `Date: ${dateStr}`,
    ...(meta.dateRange ? [`Period: ${meta.dateRange}`] : []),
    '',
  ].map((line) => `"${String(line).replace(/"/g, '""')}"`);

  const headerRow = headers.map(esc).join(',');
  const dataRows = rows.map((row) => headers.map((h) => esc(row[h])).join(','));

  const BOM = '﻿';
  return BOM + [...docRows, headerRow, ...dataRows].join('\n');
}

// Apply a createdAt (or custom field) date window to a query, if provided.
const withDateFilter = (query, dateFilter, field = 'createdAt') => {
  if (dateFilter?.startDate && dateFilter?.endDate) {
    query[field] = { $gte: new Date(dateFilter.startDate), $lte: new Date(dateFilter.endDate) };
  }
  return query;
};

const driverName = (d) => `${d.firstName || ''}${d.middleInitial ? ' ' + d.middleInitial + '.' : ''} ${d.lastName || ''}`.replace(/\s+/g, ' ').trim();

// ---------------------------------------------------------------------------
// Per-type definitions: { title, headers, rows }
// Each returns friendly, correctly-mapped columns pulled from real model fields.
// ---------------------------------------------------------------------------

export async function exportDrivers(dateFilter = {}) {
  const drivers = await Driver.find(withDateFilter({}, dateFilter)).sort({ driverId: 1 }).lean();
  const headers = ['Driver ID', 'Name', 'Email', 'Assigned Shuttle', 'License Number', 'License Expiry', 'Active', 'Created'];
  const rows = drivers.map((d) => ({
    'Driver ID': d.driverId,
    'Name': driverName(d),
    'Email': d.email,
    'Assigned Shuttle': d.shuttleId || '',
    'License Number': d.licenseNumber || '',
    'License Expiry': fmtDate(d.licenseExpiry),
    'Active': yesNo(d.isActive),
    'Created': fmtDate(d.createdAt),
  }));
  return { title: 'Drivers Report', headers, rows };
}

